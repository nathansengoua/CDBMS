    public static boolean areAllNotEmpty(Object... variables) {
        for (Object variable : variables) {
            if (isEmpty(variable)) {
                return false;
            }
        }
        return true;
    }

    // Method to check if an object is empty
    public static boolean isEmpty(Object variable) {
        if (variable == null) {
            return true;
        } else if (variable instanceof String) {
            return ((String) variable).isEmpty();
        } else if (variable instanceof Integer) {
            return (Integer) variable == 0;
        } else if (variable instanceof Date) {
            return false; // Assuming Date is not empty if it's not null
        }
        
        // Add more checks for other types as needed
        
        return false; // For other types, consider non-null as not empty
    }


   

